#ifndef _USART_H
#define _USART_H

//-----------------------------------------------------------------
// 头文件包含
//-----------------------------------------------------------------
#include <stm32f10x.h>
#include "stdio.h"	

//-----------------------------------------------------------------
// 宏定义
//-----------------------------------------------------------------
#define USART_REC_LEN  			200  				// 定义最大接收字节数 200
// 串口接收缓冲区配置
#define UART_RX_BUF_SIZE 64  // 缓冲区大小

// 全局变量声明（供main.c和usart.c共享）
extern u8 uart_rx_buf[UART_RX_BUF_SIZE];  // 接收缓冲区
extern u8 uart_rx_head;                   // 缓冲区头指针
extern u8 uart_rx_tail;                   // 缓冲区尾指针
extern u8 uart_rx_flag;                   // 接收完成标志（换行符触发）

//-----------------------------------------------------------------
// 声明
//-----------------------------------------------------------------
extern u8  USART_RX_BUF[USART_REC_LEN]; // 接收缓冲,最大USART_REC_LEN个字节.末字节为换行符 
extern u16 USART_RX_STA;         				// 接收状态标记	

//-----------------------------------------------------------------
// 外部函数声明
//-----------------------------------------------------------------
extern void uart_init(u32 bound);				// 串口初始化
#endif

